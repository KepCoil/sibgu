								<p>&amp;nbsp;&amp;nbsp;&amp;nbsp;&amp;nbsp;1.2. Положение разработано в соответствии с Федеральным законом «О высшем и послевузовском профессиональном образовании», Положением о Министерстве образования и науки Российской Федерации, Положением о подготовке научно-педагогических и научных кадров в системе послевузовского профессионального образования в Российской Федерации, утвержденным приказом Министерства образования и науки РФ, решением Высшей аттестационной комиссии и другими законодательными и нормативными правовыми актами.</p>
								<p>&amp;nbsp;&amp;nbsp;&amp;nbsp;&amp;nbsp;Основными целями подготовки КВК через аспирантуру и соискательство являются:</p>
								<p>&amp;nbsp;&amp;nbsp;&amp;nbsp;&amp;nbsp;– проведение научных исследований;</p>
								<p>&amp;nbsp;&amp;nbsp;&amp;nbsp;&amp;nbsp;– выполнение работы над кандидатской диссертацией;</p>
								<p>&amp;nbsp;&amp;nbsp;&amp;nbsp;&amp;nbsp;– сдача кандидатских экзаменов для защиты диссертации.</p>
								<p>
									<b>2. Аспирантура</b>
								</p>
								<p>&amp;nbsp;&amp;nbsp;&amp;nbsp;&amp;nbsp;Аспирантура является одной из основных форм подготовки научно-педагогических и научных кадров в системе послевузовского профессионального образования, представляющей гражданам Российской Федерации возможность повышения уровня образования, научной и педагогической квалификации.</p>
								<p>
									<b>Прием в аспирантуру</b>
								</p>
								<p>&amp;nbsp;&amp;nbsp;&amp;nbsp;&amp;nbsp;2.1. В аспирантуру принимаются граждане Российской Федерации, имеющие диплом специалиста о высшем профессиональном образовании или диплом магистра государственного образца.</p>
								<p>&amp;nbsp;&amp;nbsp;&amp;nbsp;&amp;nbsp;2.2. Прием иностранных граждан, имеющих высшее образование, и лиц без гражданства осуществляется в порядке, установленном приказом Министерства образования и науки РФ от 27.03.1998 № 814 «Об утверждении положения о подготовке научно-педагогических и научных кадров в системе послевузовского профессионального образования РФ»</p>
								<p>&amp;nbsp;&amp;nbsp;&amp;nbsp;&amp;nbsp;2.3. Для организации приема в аспирантуру СибГУ им. М.Ф. Решетнева ежегодно приказом ректора создается приемная комиссия.</p>
								<p>&amp;nbsp;&amp;nbsp;&amp;nbsp;&amp;nbsp;2.4. Прием в аспирантуру СибГУ им. М.Ф. Решетнева проводится ежегодно в соответствии с планом-графиком, утвержденным приказом ректора. Документы от поступающих принимаются до 10 февраля (1-й поток) и до 15 сентября (2-й поток) текущего года и рассматриваются приемной комиссией.</p>
								<p>&amp;nbsp;&amp;nbsp;&amp;nbsp;&amp;nbsp;2.5. Поступающий в аспирантуру проходит собеседование с предполагаемым научным руководителем, который сообщает о результате собеседования заведующему кафедрой для оформления заявки на место в аспирантуре университета.</p>
								<p>&amp;nbsp;&amp;nbsp;&amp;nbsp;&amp;nbsp;2.6. Поступающие представляют в отдел аспирантуры следующие документы:</p>
								<p>&amp;nbsp;&amp;nbsp;&amp;nbsp;&amp;nbsp;– заявление на имя ректора (форма 1);</p>
								<p>&amp;nbsp;&amp;nbsp;&amp;nbsp;&amp;nbsp;– копия диплома о высшем профессиональном образовании (специалиста, магистра) и приложения к нему;</p>
								<p>&amp;nbsp;&amp;nbsp;&amp;nbsp;&amp;nbsp;– характеристику или решение ГАК;</p>
								<p>&amp;nbsp;&amp;nbsp;&amp;nbsp;&amp;nbsp;– личный листок по учету кадров (выдается отделом аспирантуры);</p>
								<p>&amp;nbsp;&amp;nbsp;&amp;nbsp;&amp;nbsp;– список публикаций по форме 16 (если они имеются) или реферат (статья) на тему, согласованную с предполагаемым научным руководителем. Отзыв на предоставленную статью или реферат, обязательно заверяется будущим научным руководителем;</p>
								<p>&amp;nbsp;&amp;nbsp;&amp;nbsp;&amp;nbsp;– копию удостоверения о сдаче кандидатских экзаменов (если имеются);</p>
								<p>&amp;nbsp;&amp;nbsp;&amp;nbsp;&amp;nbsp;– три фотокарточки размером 3x4 см;</p>
								<p>&amp;nbsp;&amp;nbsp;&amp;nbsp;&amp;nbsp;Документ, удостоверяющий личность, и диплом о высшем профессиональном образовании поступающий в аспирантуру представляет лично в отдел аспирантуры.</p>
								<p>&amp;nbsp;&amp;nbsp;&amp;nbsp;&amp;nbsp;2.7. Решение о допуске к вступительным экзаменам выносит приемная комиссия с учетом итогов собеседования и доводит через отдел аспирантуры до сведения поступающего в недельный срок.</p>
								<p>&amp;nbsp;&amp;nbsp;&amp;nbsp;&amp;nbsp;2.8. При поступлении в аспирантуру сдаются следующие конкурсные вступительные экзамены: специальная дисциплина; история и философия науки; иностранный язык.</p>
								<p>&amp;nbsp;&amp;nbsp;&amp;nbsp;&amp;nbsp;Пересдача вступительных экзаменов не допускается.</p>
								<p>&amp;nbsp;&amp;nbsp;&amp;nbsp;&amp;nbsp;Лица, сдавшие полностью или частично кандидатские экзамены, при поступлении в аспирантуру освобождаются от соответствующих вступительных экзаменов.</p>
								<p>&amp;nbsp;&amp;nbsp;&amp;nbsp;&amp;nbsp;Сданные вступительные экзамены в аспирантуру действительны в течение календарного года.</p>
								<p>&amp;nbsp;&amp;nbsp;&amp;nbsp;&amp;nbsp;2.9. Контрольные цифры приема аспирантов, обучающихся за счет средств федерального бюджета, устанавливаются Федеральным агентством по образованию. Места, выделяемые Федеральным агентством по образованию РФ, распределяются приемной комиссией среди успешно сдавших вступительные экзамены, в соответствии с ранее поданными заявками и с учетом эффективности научного руководства по каждой специальности.</p>
								<p>&amp;nbsp;&amp;nbsp;&amp;nbsp;&amp;nbsp;2.10. Зачисление в аспирантуру производится на основании решения приемной комиссии по результатам сдачи вступительных экзаменов, собеседования с предполагаемым научным руководителем и оформляется приказом ректора по университету.</p>
