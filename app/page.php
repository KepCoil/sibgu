								<p>&amp;nbsp;&amp;nbsp;&amp;nbsp;&amp;nbsp;2.12. Аспирантам очной формы обучения, зачисленным в рамках контрольных цифр, выплачивается стипендия со дня зачисления, но не ранее дня увольнения с предыдущего места работы.</p>
								<p>&amp;nbsp;&amp;nbsp;&amp;nbsp;&amp;nbsp;Решением Ученого совета СибГУ им. М.Ф. Решетнева аспирантам могут быть назначены государственные и именные стипендии.</p>
								<p>&amp;nbsp;&amp;nbsp;&amp;nbsp;&amp;nbsp;Организация подготовки в аспирантуре</p>
								<p>&amp;nbsp;&amp;nbsp;&amp;nbsp;&amp;nbsp;2.13. Срок подготовки в очной аспирантуре 3 года, в заочной 4 года.</p>
								<p>&amp;nbsp;&amp;nbsp;&amp;nbsp;&amp;nbsp;2.14. Подготовка осуществляется на бюджетной и на договорной основе (с полным возмещением затрат).</p>
								<p>&amp;nbsp;&amp;nbsp;&amp;nbsp;&amp;nbsp;Прием лиц в аспирантуру на договорной основе производится в соответствии с действующими правилами приема. Договор на подготовку в аспирантуре может быть заключен университетом непосредственно с лицом, обучающимся в аспирантуре или с организацией, оплачивающей стоимость обучения.</p>
								<p>&amp;nbsp;&amp;nbsp;&amp;nbsp;&amp;nbsp;2.15. После зачисления аспирант в течение двух месяцев, совместно с научным руководителем, разрабатывает индивидуальный план работы, формулирует и утверждает на кафедре и Ученом совете факультета тему диссертационной работы и представляет их в отдел аспирантуры.</p>
								<p>&amp;nbsp;&amp;nbsp;&amp;nbsp;&amp;nbsp;Индивидуальные планы работы аспирантов и темы диссертаций утверждаются проректором по научной работе.</p>
								<p>&amp;nbsp;&amp;nbsp;&amp;nbsp;&amp;nbsp;2.16. Научным руководителям аспирантов выделяется учебная нагрузка из расчета 50 часов за одного аспиранта.</p>
								<p>
									<b>Контроль за подготовкой аспирантов</b>
								</p>
								<p>&amp;nbsp;&amp;nbsp;&amp;nbsp;&amp;nbsp;2.17. Контроль за подготовкой аспирантов осуществляют деканы факультетов (директора институтов), которые заслушивают отчеты аспирантов и их научных руководителей на Ученых советах факультетов.</p>
								<p>&amp;nbsp;&amp;nbsp;&amp;nbsp;&amp;nbsp;2.18. Основной формой контроля над подготовкой аспиранта является аттестация. Аттестация аспирантов проводится 2 раза в год.</p>
								<p>&amp;nbsp;&amp;nbsp;&amp;nbsp;&amp;nbsp;2.19. Промежуточная аттестация проводится через пол года, полтора года, два с половиной года, и три с половиной года для заочников. Промежуточные отчеты заслушиваются только на заседаниях кафедр.</p>
								<p>&amp;nbsp;&amp;nbsp;&amp;nbsp;&amp;nbsp;2.20. Годовая аттестация аспирантов зачисленных в аспирантуру в 1 поток проводится в апреле, зачисленных во второй поток проводится в октябре.</p>
