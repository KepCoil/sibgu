								<p>&amp;nbsp;&amp;nbsp;&amp;nbsp;&amp;nbsp;2.21. Подписанные отчеты представляются аспирантами в отдел аспирантуры в соответствии с ежегодным графиком аттестации и хранятся в течение 3-х лет в отдельном деле.</p>
								<p>&amp;nbsp;&amp;nbsp;&amp;nbsp;&amp;nbsp;2.22. Лица, не явившиеся на аттестацию в указанный срок, отчисляются из аспирантуры приказом ректора.</p>
								<p>&amp;nbsp;&amp;nbsp;&amp;nbsp;&amp;nbsp;2.23. Аспирант, не выполняющий в установленные сроки индивидуальный план без уважительной причины, не аттестуется и по представлению научного руководителя отчисляется из аспирантуры приказом ректора университета.</p>
								<p>&amp;nbsp;&amp;nbsp;&amp;nbsp;&amp;nbsp;2.24. Аспирант, отчисленный из аспирантуры до окончания срока обучения по собственному желанию (уважительным причинам), может быть восстановлен на оставшийся срок обучения приказом ректора университета при наличии бюджетных мест, либо на платной основе.</p>
								<p>&amp;nbsp;&amp;nbsp;&amp;nbsp;&amp;nbsp;2.25. Срок обучения в очной аспирантуре за счет средств бюджета в пределах стипендиального фонда продлевается приказом ректора университета на время отпуска по беременности и родам, а также на период болезни продолжительностью свыше месяца при наличии соответствующего медицинского заключения.</p>
								<p>&amp;nbsp;&amp;nbsp;&amp;nbsp;&amp;nbsp;2.26. Аспиранты имеют право:</p>
