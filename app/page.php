								<p>&amp;nbsp;&amp;nbsp;&amp;nbsp;&amp;nbsp;– опыт научно-исследовательской работы, подтвержденный публикациями, выступлениями на научно-технических конференциях;</p>
								<p>&amp;nbsp;&amp;nbsp;&amp;nbsp;&amp;nbsp;– средний балл по приложению к диплому не ниже 4.8;</p>
								<p>&amp;nbsp;&amp;nbsp;&amp;nbsp;&amp;nbsp;– награды научных олимпиад и конкурсов;</p>
